import { mkdir, rm, writeFile } from 'node:fs/promises'
import { existsSync } from 'node:fs'
import { execFile } from 'node:child_process'
import { promisify } from 'node:util'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const execFileAsync = promisify(execFile)
const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const outputDir = path.join(rootDir, 'test-output')
const browserPath = findBrowser()

const scenarios = [
  {
    fileName: '01-experience-sort-newest-first',
    title: 'Experience Sort - Newest First',
    description:
      'Current roles should appear first, followed by completed roles ordered by latest end year.',
    before: ['Contract Tools - 2020 - 2021', 'Platform Engineer - 2023 - Present', 'Gameplay Programmer - 2021 - 2022'],
    after: ['Platform Engineer - 2023 - Present', 'Gameplay Programmer - 2021 - 2022', 'Contract Tools - 2020 - 2021'],
  },
  {
    fileName: '02-experience-sort-unknown-dates-last',
    title: 'Experience Sort - Unknown Dates Last',
    description:
      'Entries without parseable years stay below dated work so unclear history does not float to the top.',
    before: ['Freelance Systems - Freelance', 'Data Tools - 2019 - 2023', 'Current Product Work - 2024 - Now'],
    after: ['Current Product Work - 2024 - Now', 'Data Tools - 2019 - 2023', 'Freelance Systems - Freelance'],
  },
  {
    fileName: '03-resume-preview-readable-text',
    title: 'Resume Preview - Readable Text PDF',
    description:
      'This PDF is generated from normal HTML text using the browser print engine, not a screenshot canvas.',
    before: ['Name: Alex Morgan', 'Title: Software Developer', 'Contact: alex@example.com | Los Angeles, CA'],
    after: ['Summary text remains selectable in browser-generated PDFs.', 'Experience bullets render as text.', 'Skills render as text instead of an image.'],
  },
]

await rm(outputDir, { recursive: true, force: true })
await mkdir(outputDir, { recursive: true })

for (const scenario of scenarios) {
  const htmlPath = path.join(outputDir, `${scenario.fileName}.html`)
  const pdfPath = path.join(outputDir, `${scenario.fileName}.pdf`)

  await writeFile(htmlPath, renderScenario(scenario))
  await execFileAsync(browserPath, [
    '--headless',
    '--disable-gpu',
    '--no-sandbox',
    `--print-to-pdf=${pdfPath}`,
    htmlPath,
  ])
}

await writeFile(
  path.join(outputDir, 'README.md'),
  [
    '# Test Output',
    '',
    'This folder is cleared every time `npm run test:pdf` starts.',
    '',
    ...scenarios.map(
      (scenario) => `- ${scenario.title}: \`${scenario.fileName}.pdf\``,
    ),
    '',
  ].join('\n'),
)

console.log(`Generated ${scenarios.length} PDF test outputs in ${outputDir}`)

function renderScenario(scenario) {
  return `<!doctype html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <title>${escapeHtml(scenario.title)}</title>
    <style>
      @page { margin: 0.65in; }
      body {
        color: #15181f;
        font-family: "Segoe UI", Arial, sans-serif;
        margin: 0;
      }
      h1 {
        border-bottom: 2px solid #15181f;
        font-size: 28px;
        margin: 0 0 12px;
        padding-bottom: 12px;
      }
      h2 {
        color: #1f6f68;
        font-size: 15px;
        margin: 24px 0 8px;
        text-transform: uppercase;
      }
      p {
        line-height: 1.45;
        margin: 0 0 14px;
      }
      ol {
        margin: 0;
        padding-left: 22px;
      }
      li {
        margin: 0 0 7px;
      }
      .meta {
        color: #4d5968;
        font-size: 13px;
      }
      .grid {
        display: grid;
        gap: 22px;
        grid-template-columns: 1fr 1fr;
      }
    </style>
  </head>
  <body>
    <h1>${escapeHtml(scenario.title)}</h1>
    <p class="meta">Generated by Resume Maker test output.</p>
    <p>${escapeHtml(scenario.description)}</p>
    <div class="grid">
      <section>
        <h2>Input Order</h2>
        ${renderList(scenario.before)}
      </section>
      <section>
        <h2>Expected Output</h2>
        ${renderList(scenario.after)}
      </section>
    </div>
  </body>
</html>`
}

function renderList(items) {
  return `<ol>${items.map((item) => `<li>${escapeHtml(item)}</li>`).join('')}</ol>`
}

function escapeHtml(value) {
  return value.replace(/[&<>"']/g, (character) => {
    const entities = {
      '&': '&amp;',
      '<': '&lt;',
      '>': '&gt;',
      '"': '&quot;',
      "'": '&#039;',
    }
    return entities[character]
  })
}

function findBrowser() {
  const candidates = [
    'C:\\Program Files (x86)\\Microsoft\\Edge\\Application\\msedge.exe',
    'C:\\Program Files\\Google\\Chrome\\Application\\chrome.exe',
  ]

  const browser = candidates.find((candidate) => existsSync(candidate))

  if (!browser) {
    throw new Error('Could not find Edge or Chrome for PDF test output.')
  }

  return browser
}
